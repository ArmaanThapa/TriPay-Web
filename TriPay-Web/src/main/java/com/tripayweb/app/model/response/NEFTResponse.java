package com.tripayweb.app.model.response;

public class NEFTResponse {
    private String name;
    private String mobileNo;
    private String email;
    private String transactionDate;
    private String transactionID;

    private String bankName;
    private String ifscCode;
    private String accountNumber;
    private String accountName;
    private String userAccount;
    private String bankAccount;
    private String amount;
    private String status;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobileNo() {
        return mobileNo;
    }

    public void setMobileNo(String mobileNo) {
        this.mobileNo = mobileNo;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTransactionDate() {
        return transactionDate;
    }

    public void setTransactionDate(String transactionDate) {
        this.transactionDate = transactionDate;
    }

    public String getTransactionID() {
        return transactionID;
    }

    public void setTransactionID(String transactionID) {
        this.transactionID = transactionID;
    }

    public String getBankName() {
        return bankName;
    }

    public void setBankName(String bankName) {
        this.bankName = bankName;
    }

    public String getIfscCode() {
        return ifscCode;
    }

    public void setIfscCode(String ifscCode) {
        this.ifscCode = ifscCode;
    }

    public String getAccountNumber() {
        return accountNumber;
    }

    public void setAccountNumber(String accountNumber) {
        this.accountNumber = accountNumber;
    }

    public String getAccountName() {
        return accountName;
    }

    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public String getUserAccount() {
        return userAccount;
    }

    public void setUserAccount(String userAccount) {
        this.userAccount = userAccount;
    }

    public String getBankAccount() {
        return bankAccount;
    }

    public void setBankAccount(String bankAccount) {
        this.bankAccount = bankAccount;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

	@Override
	public String toString() {
		return "NEFTResponse [name=" + name + ", mobileNo=" + mobileNo + ", email=" + email + ", transactionDate="
				+ transactionDate + ", transactionID=" + transactionID + ", bankName=" + bankName + ", ifscCode="
				+ ifscCode + ", accountNumber=" + accountNumber + ", accountName=" + accountName + ", userAccount="
				+ userAccount + ", bankAccount=" + bankAccount + ", amount=" + amount + ", status=" + status + "]";
	}
}
