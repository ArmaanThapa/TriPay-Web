package com.tripayweb.model.web;

public class TelcoOperatorDTO {

	private String code;
	private String name;
	private String serviceCode;
	private String topupCode;
	private String flexiCode;
	private String specialCode;
	private String validityCode;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getServiceCode() {
		return serviceCode;
	}

	public void setServiceCode(String serviceCode) {
		this.serviceCode = serviceCode;
	}

	public String getTopupCode() {
		return topupCode;
	}

	public void setTopupCode(String topupCode) {
		this.topupCode = topupCode;
	}

	public String getFlexiCode() {
		return flexiCode;
	}

	public void setFlexiCode(String flexiCode) {
		this.flexiCode = flexiCode;
	}

	public String getSpecialCode() {
		return specialCode;
	}

	public void setSpecialCode(String specialCode) {
		this.specialCode = specialCode;
	}

	public String getValidityCode() {
		return validityCode;
	}

	public void setValidityCode(String validityCode) {
		this.validityCode = validityCode;
	}

}
